#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	x;

	x = ft_size(n);
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 0 && n <= 9)
		ft_putchar (n + '0');
	else
	{
		if (n > 9)
		{
			ft_putnbr(n / 10);
			n = n % 10;
		}
		ft_putchar(n + '0');
	}
	return (x);
}
