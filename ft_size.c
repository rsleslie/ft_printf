#include "ft_printf.h"

int	ft_size(int n)
{
	int	x;
	int	i;

	i = n;
	x = 0;
	if (n <= 0)
		x = 1;
	if (n < 0)
		n *= -1;
	while (i != 0)
	{
		i = i / 10;
		x++;
	}
	return (x);
}
